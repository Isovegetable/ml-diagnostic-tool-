def generate_pdf_report(
    original_shape: tuple,
    sample_size: int,
    feature_count: int,
    target_column: str,
    best_model_name: str,
    test_r2: float,
    train_r2: float,
    cv_r2_mean: float,
    cv_r2_std: float,
    mae: float,
    rmse: float,
    suggestions: list[str],
    diagnosis: dict | None = None,
    data_quality: dict | None = None,
    watermark: str | None = None,
    metrics_df: pd.DataFrame | None = None,
    hpo_results: dict | None = None,
    all_params_by_model: dict | None = None,
    scatter_plot_base64: str | None = None,
    convergence_plot_base64: str | None = None,
    generated_date: str | None = None,
) -> bytes:
    """
    生成 PDF 格式诊断报告。

    Args: 参数含义与 generate_report_text 一致。

    Returns:
        bytes: PDF 文件字节流
    """
    pdf = _ReportPDF(watermark=watermark)
    use_cjk = pdf._use_cjk
    _tr = lambda c, e: c if use_cjk else e
    pdf.add_page()

    # ========== 封面 ==========
    pdf.ln(20)
    f = pdf._font("B", 24); pdf.set_font(*f)
    pdf.set_text_color(31, 119, 180)
    pdf.cell(0, 15, _tr("材料机器学习诊断报告", "Materials ML Diagnostic Report"), align="C", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(5)
    f = pdf._font("", 11); pdf.set_font(*f)
    pdf.set_text_color(120, 120, 120)
    pdf.cell(0, 7, _tr("生成时间：", "Generated: ") + (generated_date or datetime.now().strftime('%Y-%m-%d %H:%M')), align="C", new_x="LMARGIN", new_y="NEXT")
    pdf.cell(0, 7, _tr("目标列：", "Target: ") + target_column, align="C", new_x="LMARGIN", new_y="NEXT")
    if data_quality:
        pdf.cell(0, 7, _tr("数据集：", "Dataset: ") + data_quality.get('dataset', target_column), align="C", new_x="LMARGIN", new_y="NEXT")
    pdf.ln(15)

    # ========== 诊断等级 ==========
    if diagnosis:
        level = diagnosis["level"]
        label = _level_label(level, use_cjk)
        colors = {"A": (26, 125, 54), "B": (184, 134, 11), "C": (204, 85, 0), "D": (179, 0, 0)}
        color = colors.get(level, (100, 100, 100))
        score = diagnosis["score"]

        pdf.set_fill_color(*color)
        pdf.set_text_color(255, 255, 255)
        f = pdf._font("B", 28); pdf.set_font(*f)
        pdf.cell(30, 30, f"  {level}  ", fill=True, align="C")
        f = pdf._font("", 12); pdf.set_font(*f)
        pdf.cell(0, 30, _tr(f"  {label}   评分 {score}/100", f"  {label}   Score {score}/100"), new_x="LMARGIN", new_y="NEXT")
        pdf.ln(2)
        f = pdf._font("I", 9); pdf.set_font(*f)
        pdf.set_text_color(80, 80, 80)
        reason = diagnosis.get("reason", "")
        if not use_cjk:
            # 英文降级：显示诊断等级摘要
            reason = f"Diagnosis: {level} ({label}) - Score {score}/100"
        pdf.multi_cell(0, 5, reason)
        pdf.ln(8)

    # ========== 数据概况 ==========
    pdf.section_title("数据概况", "Data Overview")
    pdf.key_value("原始样本数", str(original_shape[0]), "Original Rows")
    pdf.key_value(_tr("有效建模样本数", "Valid Samples"), str(sample_size))
    pdf.key_value(_tr("特征数量", "Features"), str(feature_count))
    pdf.key_value(_tr("目标列", "Target"), target_column)
    if data_quality:
        dq_parts = []
        if data_quality.get("duplicate_rows", 0) > 0:
            dq_parts.append(f"Duplicates: {data_quality['duplicate_rows']}" if not use_cjk else f"重复行：{data_quality['duplicate_rows']}")
        if data_quality.get("outlier_rows", 0) > 0:
            dq_parts.append(f"Outliers: {data_quality['outlier_rows']}" if not use_cjk else f"异常值行：{data_quality['outlier_rows']}")
        if data_quality.get("near_dup_rows", 0) > 0:
            dq_parts.append(f"Near-dups: {data_quality['near_dup_rows']}" if not use_cjk else f"近重复：{data_quality['near_dup_rows']}")
        if dq_parts:
            f = pdf._font("", 9); pdf.set_font(*f)
            pdf.set_text_color(180, 100, 0)
            prefix = "  Data Quality: " if not use_cjk else "  数据质量提示："
            pdf.cell(0, 7, prefix + " | ".join(dq_parts), new_x="LMARGIN", new_y="NEXT")
            pdf.ln(2)
    pdf.ln(4)

    # ========== 最佳模型 ==========
    pdf.section_title(_tr("最佳模型", "Best Model"), _tr("最佳模型", "Best Model"))
    f = pdf._font("B", 14); pdf.set_font(*f)
    pdf.set_text_color(31, 119, 180)
    pdf.cell(0, 10, best_model_name, new_x="LMARGIN", new_y="NEXT")
    pdf.ln(2)
    pdf.metric_grid([
        (_tr("交叉验证 R2", "CV R2"), f"{cv_r2_mean:.4f}", f"±{cv_r2_std:.4f}"),
        (_tr("训练集 R2", "Train R2"), f"{train_r2:.4f}", ""),
        (_tr("测试集 R2", "Test R2"), f"{test_r2:.4f}", ""),
    ])
    pdf.metric_grid([
        ("MAE", f"{mae:.4f}", ""),
        ("RMSE", f"{rmse:.4f}", ""),
        ("", "", ""),
    ])
    pdf.ln(4)

    # ========== 模型对比表格 ==========
    if metrics_df is not None and len(metrics_df) > 1:
        pdf.check_space(60)
        pdf.section_title(_tr("模型对比", "Model Comparison"), _tr("模型对比", "Model Comparison"))
        # 表头
        headers = [_tr("模型", "Model"), "Train R²", "Test R²", "CV R²", "MAE", "RMSE"]
        col_widths = [36, 28, 28, 32, 28, 28]  # total ~180
        f = pdf._font("B", 8); pdf.set_font(*f)
        pdf.set_fill_color(31, 119, 180)
        pdf.set_text_color(255, 255, 255)
        for i, h in enumerate(headers):
            pdf.cell(col_widths[i], 7, h, border=1, fill=True, align="C")
        pdf.ln()
        # 数据行
        f_alt = pdf._font("", 8); pdf.set_font(*f_alt)
        pdf.set_text_color(50, 50, 50)
        for idx, (_, row) in enumerate(metrics_df.iterrows()):
            if idx % 2 == 1:
                pdf.set_fill_color(245, 248, 252)
                fill = True
            else:
                fill = False
            pdf.cell(col_widths[0], 6, str(row["model"]), border=1, align="C", fill=fill)
            pdf.cell(col_widths[1], 6, f"{row['train_r2']:.4f}", border=1, align="C", fill=fill)
            pdf.cell(col_widths[2], 6, f"{row['test_r2']:.4f}", border=1, align="C", fill=fill)
            pdf.cell(col_widths[3], 6, f"{row['cv_r2_mean']:.4f}±{row['cv_r2_std']:.4f}", border=1, align="C", fill=fill)
            pdf.cell(col_widths[4], 6, f"{row['mae']:.4f}", border=1, align="C", fill=fill)
            pdf.cell(col_widths[5], 6, f"{row['rmse']:.4f}", border=1, align="C", fill=fill)
            pdf.ln()
        pdf.ln(4)

    # ========== HPO 超参数优化 ==========
    if hpo_results:
        pdf.check_space(40)
        pdf.section_title(_tr("超参数优化 (HPO) 结果", "Hyperparameter Optimization"), _tr("超参数优化 (HPO) 结果", "HPO Results"))
        methods = {r["method"] for r in hpo_results.values()}
        method_str = ", ".join(m.upper() for m in methods)
        f = pdf._font("", 10); pdf.set_font(*f)
        pdf.set_text_color(80, 80, 80)
        pdf.cell(0, 7, _tr("优化方法: ", "Method: ") + method_str, new_x="LMARGIN", new_y="NEXT")
        pdf.ln(2)
        for model_name, hpo_res in hpo_results.items():
            params_str = ", ".join(f"{k}={v}" for k, v in hpo_res["best_params"].items())
            f = pdf._font("B", 9); pdf.set_font(*f)
            pdf.set_text_color(31, 119, 180)
            pdf.cell(0, 6, model_name, new_x="LMARGIN", new_y="NEXT")
            f = pdf._font("", 8); pdf.set_font(*f)
            pdf.set_text_color(60, 60, 60)
            pdf.cell(0, 5, _tr(
                f"  CV R² = {hpo_res['best_score']:.4f}, RMSE = {hpo_res.get('best_rmse', 0.0):.4f} ({hpo_res['n_trials']} 轮)",
                f"  CV R² = {hpo_res['best_score']:.4f}, RMSE = {hpo_res.get('best_rmse', 0.0):.4f} ({hpo_res['n_trials']} trials)",
            ), new_x="LMARGIN", new_y="NEXT")
            pdf.set_text_color(100, 100, 100)
            pdf.set_x(15)  # indent
            f = pdf._font("", 7); pdf.set_font(*f)
            pdf.multi_cell(0, 4, f"  {params_str}")
            if all_params_by_model and model_name in all_params_by_model:
                all_p = all_params_by_model[model_name]
                tuned_keys = set(hpo_res["best_params"].keys())
                other = {k: v for k, v in all_p.items() if k not in tuned_keys}
                if other:
                    other_str = ", ".join(f"{k}={v}" for k, v in other.items())
                    pdf.set_text_color(140, 140, 140)
                    pdf.set_x(18)
                    pdf.multi_cell(0, 4, _tr(f"其他(默认): {{{other_str}}}", f"(defaults): {{{other_str}}}"))
            pdf.ln(2)
        pdf.ln(2)

    # ========== 图表 ==========
    if scatter_plot_base64:
        try:
            pdf.check_space(85)
            scatter_bytes = base64.b64decode(scatter_plot_base64)
            scatter_buf = BytesIO(scatter_bytes)
            pdf.image(scatter_buf, x=35, w=130)
            pdf.ln(3)
        except Exception:
            pass
    if convergence_plot_base64:
        try:
            pdf.check_space(85)
            conv_bytes = base64.b64decode(convergence_plot_base64)
            conv_buf = BytesIO(conv_bytes)
            pdf.image(conv_buf, x=35, w=130)
            pdf.ln(3)
        except Exception:
            pass

    # ========== 诊断建议 ==========
    if suggestions:
        pdf.section_title(_tr("诊断建议", "Recommendations"), _tr("诊断建议", "Recommendations"))
        f = pdf._font("", 10); pdf.set_font(*f)
        pdf.set_text_color(50, 50, 50)
        if not use_cjk:
            pdf.multi_cell(0, 6, "See the full report for detailed recommendations.")
        else:
            for s in suggestions:
                pdf.cell(5, 6, "-")
                pdf.multi_cell(0, 6, s)
                pdf.ln(1)
        pdf.ln(2)

    # ========== 下一步建议 ==========
    next_steps = _next_steps_suggestions(diagnosis, test_r2, sample_size, use_cjk)
    if next_steps:
        pdf.section_title(_tr("下一步建议", "Next Steps"), _tr("下一步建议", "Next Steps"))
        f = pdf._font("", 10); pdf.set_font(*f)
        pdf.set_text_color(50, 50, 50)
        if use_cjk:
            for i, step in enumerate(next_steps, 1):
                pdf.cell(5, 6, f"{i}.")
                pdf.multi_cell(0, 6, step)
                pdf.ln(1)
        else:
            for step in next_steps:
                pdf.cell(5, 6, "-")
                pdf.multi_cell(0, 6, step)
                pdf.ln(1)
        pdf.ln(2)

    # ========== 免责声明 ==========
    pdf.ln(6)
    pdf.set_draw_color(200, 200, 200)
    pdf.line(10, pdf.get_y(), 200, pdf.get_y())
    pdf.ln(4)
    f = pdf._font("I", 8); pdf.set_font(*f)
    pdf.set_text_color(140, 140, 140)
    pdf.multi_cell(0, 4.5,
        _tr(
            "免责声明：本工具仅用于科研数据初步分析，不代表因果结论，不保证论文发表。所有结果均基于自动化模型流程生成，仅供参考。",
            "Disclaimer: This tool is for preliminary data analysis only. Results are automated and do not represent causal conclusions or publication guarantees.",
        )
    )

    return bytes(pdf.output())
