"""
PDF 报告生成模块。

使用 fpdf2 生成正式报告 PDF。
支持中文字体（自动探测 Windows/Mac/Linux 系统字体）。
"""

import base64
import os
from datetime import datetime
from io import BytesIO
from pathlib import Path

import pandas as pd
from fpdf import FPDF

# 自动探测系统可用的中文字体
# 优先 TTF 格式（兼容性更好），其次 TTC
_CJK_FONT_PATHS = [
    # Windows TTF
    "C:/Windows/Fonts/simhei.ttf",
    "C:/Windows/Fonts/deng.ttf",
    # Windows TTC
    "C:/Windows/Fonts/msyh.ttc",
    # macOS
    "/System/Library/Fonts/PingFang.ttc",
    "/System/Library/Fonts/STHeiti Light.ttc",
    # Linux (apt install fonts-noto-cjk)
    "/usr/share/fonts/opentype/noto/NotoSansCJK-Regular.ttc",
    "/usr/share/fonts/truetype/noto/NotoSansCJK-Regular.ttc",
    # Linux (apt install fonts-wqy-microhei)
    "/usr/share/fonts/truetype/wqy/wqy-microhei.ttc",
    "/usr/share/fonts/truetype/droid/DroidSansFallbackFull.ttf",
]
_CJK_FONT = None
for _p in _CJK_FONT_PATHS:
    if os.path.exists(_p):
        _CJK_FONT = _p
        break


def _add_cjk_font(pdf: FPDF, font_path: str) -> bool:
    """注册中文字体到 PDF，自动判断 TTC/TTF。"""
    try:
        is_ttc = font_path.lower().endswith(".ttc")
        # fpdf2 指定 ttc_index=0 以保证 TTC 使用第一个字体
        pdf.add_font("CJK", "", font_path, ttc_index=0) if is_ttc else pdf.add_font("CJK", "", font_path)
        # 加粗版本
        bold_path = font_path.replace(".ttc", "bd.ttc").replace(".ttf", "bd.ttf")
        if os.path.exists(bold_path):
            pdf.add_font("CJK", "B", bold_path, ttc_index=0) if is_ttc else pdf.add_font("CJK", "B", bold_path)
        else:
            pdf.add_font("CJK", "B", font_path, ttc_index=0) if is_ttc else pdf.add_font("CJK", "B", font_path)
        return True
    except Exception:
        return False


class _ReportPDF(FPDF):
    """自定义 PDF 样式（支持中文）。"""

    def __init__(self, watermark: str | None = None):
        super().__init__()
        self.set_auto_page_break(auto=True, margin=18)
        self._use_cjk = _CJK_FONT is not None and _add_cjk_font(self, _CJK_FONT)
        self._watermark = watermark

    def _font(self, style="", size=10):
        """返回可用字体名（CJK 不支持 italic 时降级为常规）。"""
        if not self._use_cjk:
            return ("Helvetica", style, size)
        safe_style = style if style in ("", "B") else ""
        return ("CJK", safe_style, size)

    def header(self):
        if self.page_no() > 1:
            f = self._font("I", 8); self.set_font(*f)
            self.set_text_color(150, 150, 150)
            self.cell(0, 6, "Materials ML Diagnostic Report" if not self._use_cjk else "材料机器学习诊断报告", align="L")
            self.cell(0, 6, f"Page {self.page_no()}", align="R", new_x="LMARGIN", new_y="NEXT")
            self.line(10, 14, 200, 14)
            self.ln(4)

    def footer(self):
        self.set_y(-15)
        f = self._font("", 7); self.set_font(*f)
        self.set_text_color(180, 180, 180)
        text = self._watermark or (
            "本报告由材料机器学习自动诊断工具生成，仅供参考"
            if self._use_cjk else "Generated by Materials ML Diagnostic Tool"
        )
        self.cell(0, 10, text, align="C")

    def section_title(self, title_cn: str, title_en: str = ""):
        title = title_en if not self._use_cjk else title_cn
        f = self._font("B", 13); self.set_font(*f)
        self.set_text_color(31, 119, 180)
        self.cell(0, 10, title, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(31, 119, 180)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(3)

    def body_text(self, text: str):
        f = self._font("", 10); self.set_font(*f)
        self.set_text_color(50, 50, 50)
        self.multi_cell(0, 6, text)
        self.ln(2)

    def check_space(self, needed_mm: float):
        """如果剩余空间不足，换页。needed_mm 是预估需要的高度。"""
        if self.get_y() + needed_mm > self.h - self.b_margin:
            self.add_page()

    def key_value(self, key_cn: str, value: str, key_en: str = ""):
        key = key_en if not self._use_cjk else key_cn
        f = self._font("", 10); self.set_font(*f)
        self.set_text_color(50, 50, 50)
        self.cell(60, 7, key, align="R")
        f = self._font("B", 10); self.set_font(*f)
        self.cell(0, 7, value, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def metric_grid(self, metrics: list[tuple[str, str, str]]):
        """三列指标卡片。"""
        x_start = self.get_x()
        y_start = self.get_y()
        self.set_fill_color(240, 244, 248)
        col_w = 57
        for i, (label, value, note) in enumerate(metrics):
            x = x_start + i * (col_w + 4)
            self.set_xy(x, y_start)
            self.rect(x, y_start, col_w, 22, style="DF")
            self.set_xy(x + 2, y_start + 2)
            f = self._font("B", 14); self.set_font(*f)
            self.set_text_color(31, 119, 180)
            self.cell(col_w - 4, 7, value)
            self.set_xy(x + 2, y_start + 10)
            f = self._font("", 8); self.set_font(*f)
            self.set_text_color(100, 100, 100)
            self.cell(col_w - 4, 5, label)
            if note:
                self.set_xy(x + 2, y_start + 15)
                f = self._font("", 7); self.set_font(*f)
                self.set_text_color(150, 150, 150)
                self.cell(col_w - 4, 4, note)
        self.set_xy(x_start, y_start + 26)


def _level_label(level: str, use_cjk: bool = True) -> str:
    labels_cn = {"A": "适合建模", "B": "可探索", "C": "高风险", "D": "不建议"}
    labels_en = {"A": "Adequate", "B": "Explorable", "C": "High Risk", "D": "Not Recommended"}
    return labels_cn.get(level, "—") if use_cjk else labels_en.get(level, "—")


